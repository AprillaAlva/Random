public class Variable03 {
    public static void main(String[] args) {
        // Deklarasi Variabel
        String nama = "Bima Arya Sena";
        String nim = "2311016210004";

        // Menulis Ke Layar
        System.out.println("NIM  : " + nim);
        System.out.println("Nama : " + nama);

        System.out.println("");
        nama = "Aprilla Alva Rifki";
        nim = "2311016210003";

        System.out.println("NIM  : " + nim);
        System.out.println("Nama : " + nama);
    }
}
